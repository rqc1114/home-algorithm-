#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <string.h>

int main(void)
{
	int k, n, m, x;
	scanf("%d %d %d", &k, &n, &m);

	x = n * k - m;

	if ((n * k - m < 0) || (n * k - m = 0))
	{

		printf("%d\n", x);
	}

	if (n * k - m > 0)
	{

		printf("%d\n", x);
	}

	return 0;
}